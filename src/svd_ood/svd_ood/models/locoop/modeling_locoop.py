    def forward(
            self,
            input_ids: Optional[torch.LongTensor] = None,
            pixel_values: Optional[torch.FloatTensor] = None,
            attention_mask: Optional[torch.Tensor] = None,
            position_ids: Optional[torch.LongTensor] = None,
            return_loss: Optional[bool] = None,
            output_attentions: Optional[bool] = None,
            output_hidden_states: Optional[bool] = None,
            return_dict: Optional[bool] = None,
    ) -> Union[Tuple, CLIPOutput]:
        r"""
        Returns:

        Examples:

        ```python
        >>> from PIL import Image
        >>> import requests
        >>> from transformers import AutoProcessor, CLIPModel

        >>> model = CLIPModel.from_pretrained("openai/clip-vit-base-patch32")
        >>> processor = AutoProcessor.from_pretrained("openai/clip-vit-base-patch32")

        >>> url = "http://images.cocodataset.org/val2017/000000039769.jpg"
        >>> image = Image.open(requests.get(url, stream=True).raw)

        >>> inputs = processor(
        ...     text=["a photo of a cat", "a photo of a dog"], images=image, return_tensors="pt", padding=True
        ... )

        >>> outputs = model(**inputs)
        >>> logits_per_image = outputs.logits_per_image  # this is the image-text similarity score
        >>> probs = logits_per_image.softmax(dim=1)  # we can take the softmax to get the label probabilities
        ```"""
        # Use CLIP model's config for some fields (if specified) instead of those of vision & text components.
        output_attentions = output_attentions if output_attentions is not None else self.config.output_attentions
        output_hidden_states = (
            output_hidden_states if output_hidden_states is not None else self.config.output_hidden_states
        )
        return_dict = return_dict if return_dict is not None else self.config.use_return_dict

        vision_outputs = self.vision_model(
            pixel_values=pixel_values,
            output_attentions=output_attentions,
            output_hidden_states=output_hidden_states,
            return_dict=return_dict,
        )

        text_outputs = self.text_model(
            input_ids=input_ids,
            attention_mask=attention_mask,
            position_ids=position_ids,
            output_attentions=output_attentions,
            output_hidden_states=output_hidden_states,
            return_dict=return_dict,
        )

        image_embeds = vision_outputs[1]
        image_embeds = self.visual_projection(image_embeds)

        text_embeds = text_outputs[1]
        text_embeds = self.text_projection(text_embeds)

        # normalized features
        image_embeds = image_embeds / image_embeds.norm(p=2, dim=-1, keepdim=True)
        text_embeds = text_embeds / text_embeds.norm(p=2, dim=-1, keepdim=True)

        # cosine similarity as logits
        logit_scale = self.logit_scale.exp()
        logits_per_text = torch.matmul(text_embeds, image_embeds.t()) * logit_scale
        logits_per_image = logits_per_text.t()

        # >>> lyx: Add for local feature >>>
        if self.compute_local_feat:
            value_states = vision_outputs.value_states
            assert value_states is not None, "Local feature is not computed."
            # local image features
            image_embeds_local = self.visual_projection(value_states)
            # normalized local image features
            image_embeds_local = image_embeds_local / image_embeds_local.norm(p=2, dim=-1, keepdim=True)
            # cosine similarity of text and local image features
            logits_per_image_local = torch.matmul(image_embeds_local, text_embeds.t()) * logit_scale
        else:
            image_embeds_local = None
            logits_per_image_local = None
        # <<< End <<<

        loss = None
        if return_loss:
            loss = clip_loss(logits_per_text)

        if not return_dict:
            output = (logits_per_image, logits_per_text, text_embeds, image_embeds, text_outputs, vision_outputs)
            return ((loss,) + output) if loss is not None else output

        return CLIPOutput(
            ## original clip output ##
            loss=loss,  # clip contrastive loss
            logits_per_image=logits_per_image,  # (images, texts), cosine similarity (with logit_scale, not softmax)
            logits_per_text=logits_per_text,  # (texts, images), cosine similarity (with logit_scale, not softmax)
            text_embeds=text_embeds,  # (texts, output_dim), nomalized text features after text_proj
            image_embeds=image_embeds,  # (images, output_dim), nomalized image features after visual_proj
            text_model_output=text_outputs,  # text_model_output dict of self.text_model(**text_inputs)
            vision_model_output=vision_outputs,  # vision_model_output dict of self.vision_model(**vision_inputs)
            ## local image features output ##
            logits_per_image_local=logits_per_image_local,  # (images, weight, height, texts)
            image_embeds_local=image_embeds_local,  # (images, weight, height, output_dim)
        )


