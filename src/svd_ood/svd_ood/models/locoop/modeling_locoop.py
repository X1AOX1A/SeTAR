        # Disable local feature computation for vision models
        def _disable_local_feat(module):
            """Apply `compute_local_feat=False` to all vision model components, including:
                - vision_model (CLIPVisionTransformer)
                - vision_model.encoder (CLIPEncoder)
                - vision_model.encoder.layers (CLIPEncoderLayer)
            """
            if hasattr(module, "compute_local_feat"):
                module.compute_local_feat = False

        self.compute_local_feat = False
        self.vision_model.apply(_disable_local_feat)

