        return_dict = return_dict if return_dict is not None else self.config.use_return_dict

        if pixel_values is None:
            raise ValueError("You have to specify pixel_values")

        hidden_states = self.embeddings(pixel_values)
        hidden_states = self.pre_layrnorm(hidden_states)

        encoder_outputs = self.encoder(
            inputs_embeds=hidden_states,
            output_attentions=output_attentions,
            output_hidden_states=output_hidden_states,
            return_dict=return_dict,
        )

        # >>> lyx: Add for local feature >>>
        if self.compute_local_feat:
            value_states = encoder_outputs.value_states
            assert value_states is not None, "Local feature is not computed."
            value_states = self.post_layernorm(value_states)
            value_states = value_states[:, 1:, :]
            B, _, C = encoder_outputs[0][:, 1:, :].size()
            grid_size = self.embeddings.image_size // self.embeddings.patch_size
            value_states = value_states.reshape(B, grid_size, grid_size, C).contiguous()
            encoder_outputs.value_states = value_states
        # <<< End <<<

        last_hidden_state = encoder_outputs[0]
        pooled_output = last_hidden_state[:, 0, :]
        pooled_output = self.post_layernorm(pooled_output)

        # >>> lyx: Add for local feature >>>
        if not return_dict:
            return tuple(v for v in [last_hidden_state, pooled_output] + encoder_outputs[1:] if v is not None)
        return BaseModelOutputWithPooling(
            last_hidden_state=last_hidden_state,
            pooler_output=pooled_output,
            hidden_states=encoder_outputs.hidden_states,
            attentions=encoder_outputs.attentions,
            value_states=encoder_outputs.value_states,
        )
