class Prompter(nn.Module):
    def __init__(self, n_ctx: int, ctx_dim: int, ctx_init: Optional[torch.Tensor] = None):
        """
        the soft prompt of locoop model
        Args:
            n_ctx: the length of the soft prompt
            ctx_dim: the dim of the prompt, always equal to the token embedding dim
            ctx_init: if is not be None, we will init the prompt by the given torch tensors
        """
        super().__init__()
        if ctx_init:
            # use given vectors to initialize prompter
            assert ctx_init.shape[0] == n_ctx, "the length of ctx_init {} is not equal to the n_ctx {}".format(
                ctx_init.shape[0], n_ctx)
            ctx_vectors = ctx_init
            logging.info("use given vectors to initializer prompter")
        else:
            # random initialization
            ctx_vectors = torch.empty(n_ctx, ctx_dim, dtype=torch.float32)
            nn.init.normal_(ctx_vectors, std=0.02)

        # logging.info(f"Number of context words (tokens): {n_ctx}")

        self.ctx = nn.Parameter(ctx_vectors)  # to be optimized
        self.n_ctx = n_ctx

    def forward(self, token_embedding: torch.Tensor, attn_mask: torch.Tensor):
        """
        Args:
            token_embedding: shape(batch, seqlen, embedding_dim)

        Returns: the token embedding with soft prompt
        """
        ctx = self.ctx
        ctx = ctx.unsqueeze(0).repeat(token_embedding.shape[0], 1, 1)  # [n_ctx,dim] -> [batch,n_ctx,dim]
        if attn_mask is not None:
            attn_mask = torch.cat(
                [torch.ones((attn_mask.shape[0], self.n_ctx), device=attn_mask.device, dtype=attn_mask.dtype),
                 attn_mask], dim=1)
        prefix = token_embedding[:, :1, :]
        suffix = token_embedding[:, 1:, :]
        prompts = torch.cat(
            [
                prefix,  # (n_cls, 1, dim)
                ctx,  # (n_cls, n_ctx, dim)
                suffix,  # (n_cls, *, dim)
            ],
            dim=1,
        )
        return prompts, attn_mask


class LoCoOpTextTransformer(CLIPTextTransformer):
    def __init__(self, config: CLIPTextConfig):
        super().__init__(config)
        self.prompt = None

    def register_prompt(self, n_ctx: int, ctx_init: Optional[torch.Tensor] = None):
        """
            register soft prompt of locoop model
            Args:
                n_ctx: the length of the soft prompt
                ctx_init: if is not be None, we will init the prompt by the given torch tensors
        """
        self.ctx_dim = self.config.hidden_size
        self.n_ctx = n_ctx
        self.prompt = Prompter(n_ctx, self.ctx_dim, ctx_init)
        self.add_module('prompt', self.prompt)
        # logging.info("Registered prompter with n_ctx = {}".format(n_ctx))

    def load_prompter(self, locoop_ckpt: str):
        """
        load LoCoOp prompt by the given args
        :param locoop_ckpt: the path of the LoCoOp prompt state dict
        :return: the model after load LoCoOp
        """
        if not locoop_ckpt:
            logging.info("Note that load_model() is skipped as no pretrained model is given")
            return
        model_path = locoop_ckpt

        if not osp.exists(model_path):
            raise FileNotFoundError('Model not found at "{}"'.format(model_path))

        checkpoint = torch.load(model_path)
        state_dict = checkpoint["state_dict"]
        epoch = checkpoint["epoch"]

        # Ignore fixed token vectors
        if "token_prefix" in state_dict:
            del state_dict["token_prefix"]

        if "token_suffix" in state_dict:
            del state_dict["token_suffix"]

        device = self.final_layer_norm.bias.device  # get the device information
        ctx = state_dict['ctx'].to(device, dtype=self.prompt.ctx.dtype)
        position_embedding = self.embeddings.position_embedding(torch.arange(1, 1 + self.n_ctx, device=device))
        self.prompt.ctx = nn.Parameter(ctx + position_embedding)

    def register_load_prompt(self, n_ctx: int, locoop_ckpt: str,
                             ctx_init: Optional[torch.Tensor] = None):
        self.register_prompt(n_ctx, ctx_init)
        self.load_prompter(locoop_ckpt)

    @add_start_docstrings_to_model_forward(CLIP_TEXT_INPUTS_DOCSTRING)
    @replace_return_docstrings(output_type=BaseModelOutputWithPooling, config_class=CLIPTextConfig)
    def forward(
            self,
            input_ids: Optional[torch.Tensor] = None,
            attention_mask: Optional[torch.Tensor] = None,
            position_ids: Optional[torch.Tensor] = None,
            output_attentions: Optional[bool] = None,
            output_hidden_states: Optional[bool] = None,
            return_dict: Optional[bool] = None,
    ) -> Union[Tuple, BaseModelOutputWithPooling]:
        r"""
        Returns:

        """
        output_attentions = output_attentions if output_attentions is not None else self.config.output_attentions
        output_hidden_states = (
            output_hidden_states if output_hidden_states is not None else self.config.output_hidden_states
        )
        return_dict = return_dict if return_dict is not None else self.config.use_return_dict

        if input_ids is None:
            raise ValueError("You have to specify input_ids")

        input_shape = input_ids.size()
        input_ids = input_ids.view(-1, input_shape[-1])
        if self.prompt is not None:
            position_ids = torch.arange(0, input_ids.shape[1], device=input_ids.device).unsqueeze(0)
            position_ids[:, 1:] = position_ids[:, 1:] + self.n_ctx
        hidden_states = self.embeddings(input_ids=input_ids, position_ids=position_ids)
        # add for record the length of soft prompt
        prompt_len = 0
        if self.prompt is not None:
            hidden_states, attention_mask = self.prompt(hidden_states, attention_mask)
            prompt_len = self.n_ctx
            input_shape = torch.Size((input_shape[0], input_shape[1] + prompt_len))
        # CLIP's text model uses causal mask, prepare it here.
        # https://github.com/openai/CLIP/blob/cfcffb90e69f37bf2ff1e988237a0fbe41f33c04/clip/model.py#L324
        causal_attention_mask = _create_4d_causal_attention_mask(
            input_shape, hidden_states.dtype, device=hidden_states.device
        )
        # expand attention_mask
        if attention_mask is not None:
            # [bsz, seq_len] -> [bsz, 1, tgt_seq_len, src_seq_len]
            attention_mask = _prepare_4d_attention_mask(attention_mask, hidden_states.dtype)

        encoder_outputs = self.encoder(
            inputs_embeds=hidden_states,
            attention_mask=attention_mask,
            causal_attention_mask=causal_attention_mask,
            output_attentions=output_attentions,
            output_hidden_states=output_hidden_states,
            return_dict=return_dict,
        )

        last_hidden_state = encoder_outputs[0]
        last_hidden_state = self.final_layer_norm(last_hidden_state)

        if self.eos_token_id == 2:
            # The `eos_token_id` was incorrect before PR #24773: Let's keep what have been done here.
            # A CLIP model with such `eos_token_id` in the config can't work correctly with extra new tokens added
            # ------------------------------------------------------------
            # text_embeds.shape = [batch_size, sequence_length, transformer.width]
            # take features from the eot embedding (eot_token is the highest number in each sequence)
            # casting to torch.int for onnx compatibility: argmax doesn't support int64 inputs with opset 14
            pooled_output = last_hidden_state[
                torch.arange(last_hidden_state.shape[0], device=last_hidden_state.device),
                input_ids.to(dtype=torch.int, device=last_hidden_state.device).argmax(dim=-1) + prompt_len,
            ]
        else:
            # The config gets updated `eos_token_id` from PR #24773 (so the use of exta new tokens is possible)
            pooled_output = last_hidden_state[
                torch.arange(last_hidden_state.shape[0], device=last_hidden_state.device),
                # We need to get the first position of `eos_token_id` value (`pad_token_ids` might equal to `eos_token_id`)
                (input_ids.to(dtype=torch.int, device=last_hidden_state.device) == self.eos_token_id)
                    .int()
                    .argmax(dim=-1) + prompt_len,  # we will insert the prompt at the begin of the hidden state
            ]

        if not return_dict:
            return (last_hidden_state, pooled_output) + encoder_outputs[1:]

        return BaseModelOutputWithPooling(
            last_hidden_state=last_hidden_state,
            pooler_output=pooled_output,
            hidden_states=encoder_outputs.hidden_states,
            attentions=encoder_outputs.attentions,
        )


